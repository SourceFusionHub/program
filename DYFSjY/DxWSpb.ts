function calculateCubeArea(sideLength: number): number {
  return 6 * sideLength ** 2;
}


// example usage
const sideLength = 5;
const cubeArea = calculateCubeArea(sideLength);

console.log(`The surface area of the cube is ${cubeArea} square units.`);

// output - The surface area of the cube is 150 square units
