// Define a function to calculate the product of two numbers
func calculateProduct(num1: Int, num2: Int) -> Int {
    return num1 * num2  // Return the product of num1 and num2
}

// Input numbers
let number1 = 10
let number2 = 5

// Call the function to calculate the product
let product = calculateProduct(num1: number1, num2: number2)

// Print the result
print("The product of \(number1) and \(number2) is: \(product)")
